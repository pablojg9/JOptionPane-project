package com.projeto.executaveis;

public class PositionArray {
    public static void main(String[] args) {

        double[] valor = {
            9.5, 6.4, 10
        };

        for(double value : valor) {
            System.out.println(value);
        }

        String[] palavras = {
                "Pablo",
                "Junior",
        };

        for (String palavra : palavras) {
            System.out.println(palavra);
        }
    }
}
